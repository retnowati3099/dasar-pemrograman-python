# menggunakan tanda petik dua ("")
# string sebaris
string_1 = "hello python"

# string multi-baris
string_2 = """Selamat
Belajar
Python"""
print(string_1, string_2)

# menggunakan tanda petik satu ('')
# string sebaris 
string_3 = 'for the horde!'

# string multi-baris
string_4 = '''
Sesuk
Preiiii
'''
print(string_3, string_4)

string_5 = '''\
Sesuk Prei
'''
print(string_5)

# tipe data boolean
bool_1 = True
bool_2 = False
print(bool_1, bool_2)

'''
- Literal string ditandai tanda awalan dan akhiran tanda " atau '
- Jika ada baris baru (newline) di bagian awal penulisan tanda petik tiga tunggal atau tanda petik tiga ganda, maka baris baru tersebut merupakan bagian dari string. Jika ingin meng-exclude-nya bisa menggunakan tanda backslash setelah tanda petik tiga tersebut
'''